package com.mothsoft.alexis.rest.document.v1;

import javax.xml.bind.annotation.XmlRootElement;

import org.codehaus.jackson.annotate.JsonTypeName;

@JsonTypeName("Tweet")
@XmlRootElement(name = "Tweet")
public class Tweet extends Document {

    private Long tweetId;
    private String screenName;
    private String fullName;
    private String profileImageUrl;
    private boolean retweet;
    private String retweetUserName;
    private String formattedText;

    public Tweet() {
        super();
    }

    public Long getTweetId() {
        return tweetId;
    }

    public void setTweetId(Long tweetId) {
        this.tweetId = tweetId;
    }

    public String getScreenName() {
        return screenName;
    }

    public void setScreenName(String screenName) {
        this.screenName = screenName;
    }

    public String getFullName() {
        return fullName;
    }

    public void setFullName(String fullName) {
        this.fullName = fullName;
    }

    public String getProfileImageUrl() {
        return profileImageUrl;
    }

    public void setProfileImageUrl(String profileImageUrl) {
        this.profileImageUrl = profileImageUrl;
    }

    public boolean isRetweet() {
        return retweet;
    }

    public void setRetweet(boolean retweet) {
        this.retweet = retweet;
    }

    public String getRetweetUserName() {
        return retweetUserName;
    }

    public void setRetweetUserName(String retweetUserName) {
        this.retweetUserName = retweetUserName;
    }

    public String getFormattedText() {
        return formattedText;
    }

    public void setFormattedText(String formattedText) {
        this.formattedText = formattedText;
    }

}
